    private void preencherTabela() {
        CelasController controller = new CelasController();
        ArrayList<CelasModel> lista = controller.selecionarTodos();

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "NENHUMA CELA CADASTRADA! (CelasView ln 41)",
                    "Preenchendo tabela....", JOptionPane.INFORMATION_MESSAGE);

        } else {
            DefaultTableModel modeloTabela = (DefaultTableModel) tb_celas.getModel();
            modeloTabela.setRowCount(0);
            for (CelasModel c : lista) {
                modeloTabela.addRow(new String[]{
                    String.valueOf(c.getIdcelas()),
                    c.getQtde_max(),
                    c.getQtde_atua(),
                    c.getVisita().getParentesco()
                });
            }
        }
    }

