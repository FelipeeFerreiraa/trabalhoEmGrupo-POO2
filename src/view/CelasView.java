    private void tb_celasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_celasMouseClicked
        linha = tb_celas.getSelectedRow();

        if (linha != -1) {
            cxt_id.setText(tb_celas.getValueAt(linha, 0).toString());
            cxt_max.setText(tb_celas.getValueAt(linha, 1).toString());
            cxt_atual.setText(tb_celas.getValueAt(linha, 2).toString());
            cxt_visitas.setSelectedItem(tb_celas.getValueAt(linha, 3).toString());

            btn_novo.setEnabled(false);
            btn_salvar.setEnabled(false);
            btn_editar.setEnabled(true);
            btn_excluir.setEnabled(true);

            cxt_id.setEnabled(false);
            cxt_max.setEnabled(true);
            cxt_atual.setEnabled(true);
            cxt_visitas.setEnabled(true);

            linha = -1;
        }
    }//GEN-LAST:event_tb_celasMouseClicked


