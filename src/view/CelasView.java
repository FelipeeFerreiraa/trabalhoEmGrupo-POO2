    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        String max = cxt_max.getText();
        String atual = cxt_atual.getText();
        String visita = cxt_visitas.getSelectedItem().toString();

        if (max.isEmpty() || atual.isEmpty() || visita.isEmpty()) {
            JOptionPane.showMessageDialog(this, "INFORME TODOS OS CAMPOS!", "Atenção....", JOptionPane.WARNING_MESSAGE);

        } else {

            String visitaCBX = (String) cxt_visitas.getSelectedItem();
            int intVisita = 0;
            for (int x = 0; x < listaVisitantes.size(); x++) {
                if (visitaCBX.equals(listaVisitantes.get(x).getParentesco())) {
                    intVisita = listaVisitantes.get(x).getIdvisitas();
                    break;
                }
            }

            CelasModel celas = new CelasModel();
            celas.setQtde_max(max);
            celas.setQtde_atua(atual);
            celas.getVisita().setIdvisitas(intVisita);

            // ------------ CONTROLLER
            CelasController controller = new CelasController();
            if (controller.inserir(celas)) {
                JOptionPane.showMessageDialog(this, "CELA CADASTRADA!", "Salvamento realizado...",
                        JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
                inicializar();
                preencherTabela();

            } else {
                JOptionPane.showMessageDialog(this, "ERRO AO INSERIR CELA", "Algo de errado não está certo....",
                        JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

