    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        CelasModel celas = new CelasModel();

        if (cxt_id.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ESCOLHA UMA LINHA PARA EXCLUIR!", "Excluindo....",
                    JOptionPane.INFORMATION_MESSAGE);

        } else {

            celas.setIdcelas(Integer.parseInt(cxt_id.getText()));

            // ---------------- CONTROLLER
            CelasController controller = new CelasController();
            if (controller.excluir(celas)) {
                JOptionPane.showMessageDialog(this, "Dados excluídos", "excluindo...!",
                        JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
                inicializar();
                preencherTabela();

            } else {
                JOptionPane.showMessageDialog(this, "ERRO AO EXCLUIR CELAS",
                        "Algo de errado não está certo....", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

