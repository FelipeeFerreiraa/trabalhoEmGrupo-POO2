    private void preencherComboBox() {
        VisitasController controller = new VisitasController();
        listaVisitantes = controller.selecionarTodos();
        for (VisitasModel v : listaVisitantes) {
            cxt_visitas.addItem(v.getParentesco());
        }
    }

