    private void preencherTabela() {
        IPLController controller = new IPLController();
        ArrayList<IPLModel> lista = controller.selecionarTodos();

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "NENHUM IPL CADASTRADO! (IPLView ln 41)",
                    "Preenchendo tabela....", JOptionPane.INFORMATION_MESSAGE);

        } else {
            DefaultTableModel modeloTabela = (DefaultTableModel) tb_ipl.getModel();
            modeloTabela.setRowCount(0);
            for (IPLModel i : lista) {
                modeloTabela.addRow(new String[]{
                    String.valueOf(i.getIdipl()),
                    i.getIpl_cpf(),
                    i.getIpl_nome(),
                    i.getCrime().getDescricao(),
                    i.getParentesco().getParentesco(),
                    i.getRefeicao().getComida(),
                    i.getPolicial().getNome()
                });
            }
        }
    }

