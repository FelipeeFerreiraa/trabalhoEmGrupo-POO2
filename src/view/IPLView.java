    private void tb_iplMouseClicked(java.awt.event.MouseEvent evt) {
        linha = tb_ipl.getSelectedRow();

        if (linha != -1) {
            cxt_id.setText(tb_ipl.getValueAt(linha, 0).toString());
            cxt_cpf.setText(tb_ipl.getValueAt(linha, 1).toString());
            cxt_nome.setText(tb_ipl.getValueAt(linha, 2).toString());
            cxt_crime.setSelectedItem(tb_ipl.getValueAt(linha, 3).toString());
            cxt_parentesco.setSelectedItem(tb_ipl.getValueAt(linha, 4).toString());
            cxt_refeicao.setSelectedItem(tb_ipl.getValueAt(linha, 5).toString());
            cxt_policial.setSelectedItem(tb_ipl.getValueAt(linha, 6).toString());

            btn_novo.setEnabled(false);
            btn_salvar.setEnabled(false);
            btn_editar.setEnabled(true);
            btn_excluir.setEnabled(true);

            cxt_id.setEnabled(false);
            cxt_cpf.setEnabled(true);
            cxt_nome.setEnabled(true);
            cxt_crime.setEnabled(true);
            cxt_parentesco.setEnabled(true);
            cxt_refeicao.setEnabled(true);
            cxt_policial.setEnabled(true);

            linha = -1;

        }
    }

